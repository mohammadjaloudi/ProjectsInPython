import shapes

s = shapes.Shapes

print("Square Area:", s.square_area(5, 5))
print("Square Perimeter:", s.square_perimeter(5, 5))
print("Rectangle Area:", s.rectangle_area(4, 6))
print("Rectangle Perimeter:", s.rectangle_perimeter(4, 6))
print("Circle Area:", s.circle_area(3))
print("Circle Perimeter:", s.circle_perimeter(3))
print("Triangle Area:", s.triangle_area(4, 6))
print("Triangle Perimeter:", s.triangle_perimeter(3, 4, 5))
